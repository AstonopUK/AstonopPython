from functools import reduce

def split(mylist):
    newlist = []
    for x in range(int(len(mylist)/10)):
        templist = []
        for y in range(10):
            templist.append(mylist.pop(0))
        newlist.append(templist)
    return newlist

def subFifty(x):
    return x-0.5

def mapToList(mylist):
    for x in range(len(mylist)):
        mylist[x] = list(map(subFifty, mylist[x]))
    return mylist

def add(x,y):
    return x+y

def reduceToNum(mylist):
    for x in range(len(mylist)):
        mylist[x] = round(reduce(add, mylist[x]),2)
    return mylist

